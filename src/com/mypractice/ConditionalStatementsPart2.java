package com.mypractice;

public class ConditionalStatementsPart2 {

	public static void main(String[] args) {
		int i = 3;
		switch(i) {
		case 1:
			System.out.println("This is the first element");
		break;
		case 2:
			System.out.println("This is the Second element");
		break;
		case 3:
			System.out.println("This is the Third element");
		break;
		default:
			System.out.println("This is the default element");
		break;
		
			
			
		}

	}

}
